     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $g = new Gasto();
        $g->amount = Donation::convertToCopper($request->input('oro'),$request->input('plata'),$request->input('cobre'));
        $g->comments = $request->input('comentarios');
        $g->save();

        return redirect('gastos');
    }

